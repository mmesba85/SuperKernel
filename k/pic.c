#include <k/kstd.h>

#include "multiboot.h"
#include "io.h"
#include "idt.h"

void init_pic(void)
{
  /* ICW1 */
	outb(0x20 , 0x11);
	outb(0xA0 , 0x11);

	/* ICW2 */
	outb(0x21 , 0x20);
	outb(0xA1 , 0x28);

	/* ICW3 */
	outb(0x21 , 0x00);
	outb(0xA1 , 0x00);

	/* ICW4 */
	outb(0x21 , 0x01);
	outb(0xA1 , 0x01);

	/* mask interrupts */
	outb(0x21 , 0xff);
	outb(0xA1 , 0xff);

  /* Keyboard */
  outb(0x21 , 0xFD);

  __asm__("sti\n");
}
